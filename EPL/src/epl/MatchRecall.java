package epl;

public class MatchRecall
{
    private int homeTeamRecallF;
    private int awayTeamRecallF;
    private int homeTeamScoreRecallF;
    private int awayTeamScoreRecallF;

    public int getHomeTeamRecallF() { return homeTeamRecallF; }
    public int getAwayTeamRecallF() { return awayTeamRecallF; }
    public int getHomeTeamScoreRecallF() { return homeTeamScoreRecallF; }
    public int getAwayTeamScoreRecallF() { return awayTeamScoreRecallF; }

    public void setHomeTeamRecallF(int homeTeamRecallF)
    {
        this.homeTeamRecallF = homeTeamRecallF;
    }
    public void setAwayTeamRecallF(int awayTeamRecallF) { this.awayTeamRecallF = awayTeamRecallF; }
    public void setHomeTeamScoreRecallF(int homeTeamScoreRecallF) { this.homeTeamScoreRecallF = homeTeamScoreRecallF; }
    public void setAwayTeamScoreRecallF(int awayTeamScoreRecallF) { this.awayTeamScoreRecallF = awayTeamScoreRecallF; }
}
