package model;

public class Computer
{
    private String computer_ID;
    private String computer_Model_Code;
    private String computer_Model_Name;
    private String computer_Name;
    private String factory;
    private String computer_Brand_Code;
    private String computer_Brand_Name;

    //getter
    public String getComputer_ID() {
        return computer_ID;
    }

    public String getComputer_Model_Code() {
        return computer_Model_Code;
    }

    public String getComputer_Model_Name() {
        return computer_Model_Name;
    }

    public String getComputer_Name() {
        return computer_Name;
    }

    public String getFactory() {
        return factory;
    }

    public String getComputer_Brand_Code() {
        return computer_Brand_Code;
    }

    public String getComputer_Brand_Name() {
        return computer_Brand_Name;
    }

    //setter
    public void setComputer_ID(String computer_ID) {
        this.computer_ID = computer_ID;
    }

    public void setComputer_Model_Code(String computer_Model_Code) {
        this.computer_Model_Code = computer_Model_Code;
    }

    public void setComputer_Model_Name(String computer_Model_Name) {
        this.computer_Model_Name = computer_Model_Name;
    }

    public void setComputer_Name(String computer_Name) {
        this.computer_Name = computer_Name;
    }

    public void setFactory(String factory) {
        this.factory = factory;
    }

    public void setComputer_Brand_Code(String computer_Brand_Code) {
        this.computer_Brand_Code = computer_Brand_Code;
    }

    public void setComputer_Brand_Name(String computer_Brand_Name) {
        this.computer_Brand_Name = computer_Brand_Name;
    }

    
}
